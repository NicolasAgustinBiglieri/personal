# Cadena (String)
miGrupoFavorito = "Hiromi"
comentario = "Best Jazz Composer"
print("Mi grupo favorito es: " + miGrupoFavorito + " " + comentario)

# Separar strings con "," agrega un espacio en el resultado
miGrupoFavorito = "Hiromi"
comentario = "Best Jazz Composer"
print("Mi grupo favorito es:", miGrupoFavorito, comentario)

# usar + en print genera concatenación a si lo usamos entre Strings
numero1 = "1"
numero2 = "2"
print(numero1 + numero2)

# usar + en print genera suma a si lo usamos entre int o float (valores númericos)
numero1 = 1.5
numero2 = 2
print(numero1 + numero2)

# usar + en print genera suma a si lo usamos entre int o float (valores númericos)
numero1 = "1"
numero2 = "2"
print(int(numero1) + int(numero2))
