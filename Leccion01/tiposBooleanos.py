# Tipos bool (boolean)
miVariable = False
print(miVariable)

miVariable = 3 > 2
print(miVariable)

miVariable = 1 > 2
print(miVariable)

# el if se ejecuta si la variable tipo bool es verdadera
if miVariable:
    print("El resultado fue verdadero")
else:
    print("El resultado fue falso")

